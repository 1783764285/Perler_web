from skimage.color import rgb2lab
from scipy.spatial import KDTree

# 提取色号
color_codes = [c for c, _ in perler_colors]

# RGB 转 numpy
color_rgb = np.array(
    [rgb for _, rgb in perler_colors],
    dtype=np.float32
) / 255.0

# RGB → LAB（专业颜色空间）
color_lab = rgb2lab(
    color_rgb.reshape(1, -1, 3)
).reshape(-1, 3)

# KDTree 加速搜索
tree = KDTree(color_lab)

# 色号 → RGB 字典
color_dict = {
    code: rgb
    for code, rgb in perler_colors
}

# 专业颜色匹配
def find_closest(pixel):
    pixel = np.array(pixel, dtype=np.float32) / 255.0

    pixel_lab = rgb2lab(
        pixel.reshape(1, 1, 3)
    ).reshape(1, -1)

    dist, idx = tree.query(pixel_lab, k=1)

    i = idx[0]

    return (
        (color_rgb[i] * 255).astype(np.uint8),
        color_codes[i]
    )

# 自动生成尺寸（按比例）
def auto_size(img, max_size=40):
    w, h = img.size
    ratio = min(max_size / w, max_size / h)
    return int(w * ratio), int(h * ratio)

# 生成拼豆图（带网格+色号）
def to_perler(img, width, height, cell_size=30):
    img = img.convert("RGB")
    img_small = img.resize((width, height), Image.NEAREST)
    arr = np.array(img_small)
    code_map = []

    for y in range(height):
        row = []
        for x in range(width):
            rgb, code = find_closest(arr[y, x])
            row.append(code)
        code_map.append(row)

    # 绘制网格图
    img_w = width * cell_size
    img_h = height * cell_size
    grid_img = Image.new("RGB", (img_w, img_h), "white")
    draw = ImageDraw.Draw(grid_img)

    # 加载字体（系统默认）
    try:
        font = ImageFont.truetype("simhei.ttf", 12)
    except:
        font = ImageFont.load_default()

    # 画格子+色号
    for y in range(height):
        for x in range(width):
            code = code_map[y][x]
            x1 = x * cell_size
            y1 = y * cell_size
            x2 = x1 + cell_size
            y2 = y1 + cell_size

            # 填充颜色
            rgb = color_dict[code]
            draw.rectangle([x1, y1, x2, y2], fill=rgb, outline="gray")

            # 写色号（深色背景用白色字，浅色用黑色）
            brightness = sum(rgb) / 3
            text_color = "white" if brightness < 128 else "black"
            draw.text((x1 + 5, y1 + 5), code, fill=text_color, font=font)

        # =========================
    # 底部色号统计栏
    # =========================

    color_count = count_colors(code_map)
        # =========================
    # 制作时间估算
    # =========================

    total_beads = sum(color_count.values())

    # 假设每颗拼豆平均3秒
    total_seconds = total_beads * 3

    total_minutes = round(total_seconds / 60, 1)

    total_hours = round(total_minutes / 60, 1)
    sorted_count = sorted(
        color_count.items(),
        key=lambda x: x[1],
        reverse=True
    )

    legend_height = 120

    final_img = Image.new(
        "RGB",
        (img_w, img_h + legend_height),
        (240, 240, 240)
    )

    final_img.paste(grid_img, (0, 0))

    legend_draw = ImageDraw.Draw(final_img)
    legend_draw.text(
        (20, img_h + 5),
        f"总拼豆数: {total_beads}颗   |   预计制作时间: {total_hours}小时",
        fill="black",
        font=font
    )

    start_y = img_h + 20
    x_offset = 20

    for code, cnt in sorted_count:

        rgb = color_dict[code]

        # 彩色块
        legend_draw.rounded_rectangle(
            [x_offset, start_y, x_offset + 120, start_y + 40],
            radius=10,
            fill=rgb
        )

        # 自动文字颜色
        brightness = sum(rgb) / 3
        text_color = "white" if brightness < 128 else "black"

        # 文字
        legend_draw.text(
            (x_offset + 10, start_y + 10),
            f"{code} ({cnt})",
            fill=text_color,
            font=font
        )

        x_offset += 140

        # 自动换行
        if x_offset > img_w - 140:
            x_offset = 20
            start_y += 50

    grid_img = final_img
    return final_img, code_map, width, height

# 统计色号数量
def count_colors(code_map):
    flat = [code for row in code_map for code in row]
    return Counter(flat)

